package com.comp2042;

/**
 * Enumeration representing the specific type of movement or action requested.
 */
public enum EventType {
    /** Request to move the brick downwards. */
    DOWN,

    /** Request to move the brick to the left. */
    LEFT,

    /** Request to move the brick to the right. */
    RIGHT,

    /** Request to rotate the brick. */
    ROTATE
}
